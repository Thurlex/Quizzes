/*
11. Verificando o Tipo de uma Variável (typeof)
Use typeof para exibir no console o tipo de cada constante criada no exercício anterior.
*/

const nome = "Alex";
const idade = 18;
const estudandoJavaScript = true;
const valorNulo = null;
const variavelIndefinida = undefined;
console.log("Tipo de Nome: " + typeof nome);
console.log("Tipo de Idade: " + typeof idade);
console.log("Tipo de Estudando JavaScript: " + typeof estudandoJavaScript);
console.log("Tipo de Valor Nulo: " + typeof valorNulo);
console.log("Tipo de Variavel Indefinida: " + typeof variavelIndefinida);