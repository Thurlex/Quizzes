/*
12. Operadores de Atribuição

Crie uma variável (var) numero e atribua a ela o valor 10.

Depois, use os operadores de atribuição (+=, -=, *=, /=) para modificar seu valor e exiba o resultado no console.
*/

var numero = 10;
console.log("O valor inicial de numero eh: " + numero);
numero += 23;
console.log("O valor de numero += 23 eh: " + numero);
numero -= 13;
console.log("O valor de numero -= 13 eh: " + numero);
numero *= 7.5;
console.log("O valor de numero *= 5 eh: " + numero);
numero /= 15;
console.log("O valor de numero /= 15 eh: " + numero);